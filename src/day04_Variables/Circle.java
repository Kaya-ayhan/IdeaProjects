package day04_Variables;

public class Circle {
    public static void main(String[] args) {
        double pi=3.14;
        double diameter=4.8;
        double area=pi*diameter*diameter/4;
        double perimeter=4*pi*diameter;
        System.out.println("perimeter = " + perimeter);
        System.out.println("area = " + area);
    }
}
